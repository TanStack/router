import { createWriteStream } from 'node:fs'
import path from 'node:path'
import { createCB } from 'xmlbuilder2'
import type { Page } from './schema'
import type { XMLBuilderCB } from 'xmlbuilder2/lib/interfaces'

export interface SitemapWriter {
  write: (page: Page) => void
  close: () => Promise<void>
}

export function createSitemapWriter({
  host,
  outputPath,
  publicDir,
}: {
  host: string
  outputPath: string
  publicDir: string
}): SitemapWriter {
  const slash = host.endsWith('/') ? '' : '/'
  const stream = createWriteStream(path.join(publicDir, outputPath))

  let endResolve!: () => void
  let endReject!: (err: Error) => void
  const endPromise = new Promise<void>((resolve, reject) => {
    endResolve = resolve
    endReject = reject
  })

  stream.on('error', endReject)

  const builder: XMLBuilderCB = createCB({
    prettyPrint: true,
    data: (chunk: string) => {
      stream.write(chunk)
    },
    end: () => {
      stream.end(() => endResolve())
    },
    error: (err: Error) => {
      stream.destroy(err)
      endReject(err)
    },
  })

  builder.dec({ version: '1.0', encoding: 'UTF-8' })
  builder.com('This file was automatically generated by TanStack Start.')
  builder.ele('urlset', {
    xmlns: 'https://www.sitemaps.org/schemas/sitemap/0.9',
    'xmlns:xhtml': 'http://www.w3.org/1999/xhtml',
    'xmlns:image': 'http://www.google.com/schemas/sitemap-image/1.1',
    'xmlns:news': 'http://www.google.com/schemas/sitemap-news/0.9',
  })

  return {
    write(page) {
      if (page.sitemap?.exclude === true) return

      const today = new Date().toISOString().split('T')[0]!
      const lastmod = page.sitemap?.lastmod
        ? new Date(page.sitemap.lastmod).toISOString().split('T')[0]!
        : today

      builder.ele('url')
      builder
        .ele('loc')
        .txt(`${host}${slash}${page.path.replace(/^\/+/g, '')}`)
        .up()
      builder.ele('lastmod').txt(lastmod).up()

      if (page.sitemap?.priority !== undefined) {
        builder.ele('priority').txt(page.sitemap.priority.toString()).up()
      }
      if (page.sitemap?.changefreq) {
        builder.ele('changefreq').txt(page.sitemap.changefreq).up()
      }

      if (page.sitemap?.alternateRefs?.length) {
        for (const ref of page.sitemap.alternateRefs) {
          builder
            .ele('xhtml:link')
            .att('rel', 'alternate')
            .att('href', ref.href)
          if (ref.hreflang) {
            builder.att('hreflang', ref.hreflang)
          }
          builder.up()
        }
      }

      if (page.sitemap?.images?.length) {
        for (const image of page.sitemap.images) {
          builder.ele('image:image')
          builder.ele('image:loc').txt(image.loc).up()
          if (image.title) {
            builder.ele('image:title').txt(image.title).up()
          }
          if (image.caption) {
            builder.ele('image:caption').txt(image.caption).up()
          }
          builder.up()
        }
      }

      if (page.sitemap?.news) {
        const news = page.sitemap.news
        builder.ele('news:news')
        builder.ele('news:publication')
        builder.ele('news:name').txt(news.publication.name).up()
        builder.ele('news:language').txt(news.publication.language).up()
        builder.up()
        builder
          .ele('news:publication_date')
          .txt(new Date(news.publicationDate).toISOString().split('T')[0]!)
          .up()
        builder.ele('news:title').txt(news.title).up()
        builder.up()
      }

      builder.up()
    },
    async close() {
      builder.end()
      await endPromise
    },
  }
}
